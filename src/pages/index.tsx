import Head from "next/head";
import { api } from "~/utils/api";
import { SignInButton, useUser } from "@clerk/nextjs";
import { useRouter } from "next/router";
import { useEffect } from "react";

export default function Home() {
  const { data } = api.games.getAll.useQuery();
  const user = useUser();
  const router = useRouter();

  useEffect(() => {
    if (user.isSignedIn) {
      router.push("/home").catch(() => console.error("ERROR CAN'T GO HOME"));
    }
  }, [user.isSignedIn, router]);

  return (
    <>
      <Head>
        <title>PlayScripe - Track Your Gaming Backlog</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="h-screen bg-gray-900 flex items-center justify-center">
        <div className="flex w-full max-w-4xl flex-col md:flex-row items-center bg-gray-800 rounded-2xl shadow-2xl overflow-hidden">
          <div className="md:w-1/2 w-full p-8 md:p-12 text-center flex flex-col items-center justify-center bg-gray-700">
            <h1 className="text-3xl font-bold text-white mb-4">
              Welcome to PlayScripe
            </h1>
            <p className="text-sm text-gray-300 mb-8 italic">
              Letterboxd, but for your gaming backlog.
            </p>
            {!user.isSignedIn && (
              <SignInButton>
                <button className="px-6 py-3 bg-blue-600 text-white font-medium text-lg rounded-lg shadow-lg hover:bg-blue-700 transition-all">
                  Sign In to Get Started
                </button>
              </SignInButton>
            )}
          </div>
          <div className="md:w-1/2 px-8 py-20 flex items-center bg-LastOfUs2 bg-cover bg-center"></div>
          </div>
      </main>
    </>
  );
}
